package day02;
/*
 * if-else 구조 
 * if-else 구조는 
 * 만약 if의 조건식이 false가 나오면 무조건 else의 코드블락이 실행된다. 
 * 
 */
public class Ex09 {

	public static void main(String[] args) {
		int number = 15; 
		
		if(number % 2 == 0) {
			System.out.println("짝수");
		}else {
			System.out.println("홀수");
		}

	}

}
